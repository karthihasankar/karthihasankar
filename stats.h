/*********************************************************************
 * Function: find_median
 * Description:
 * 	To calculate the median first we need to sort the list in 
 * 	ascending or descending order.
 * 	If the datas of elements are even, then the median will the
 * 	average of two numbers in the middle.
 * 	But the datas of elements are odd, then the middle element
 * 	of the array after sorting will be considered as the median.
 * Parameters:
 *	unsigned char *array: pointer to a data set
 *	unsigned int length: length of data set
 * Return:
 * 	median of the datas provided.
 *********************************************************************/

